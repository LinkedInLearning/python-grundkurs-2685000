#!/usr/bin/env python3

# Rechenoperationen mit Python

# Variablen
a = 10
b = 5
c = 3

# Addition
summe = a + b
print("Summe:", summe)

# Subtraktion
differenz = a - b
print("Differenz:", differenz)

# Multiplikation
produkt = a * b
print("Produkt:", produkt)

# Division
quotient = a / b
print("Quotient:", quotient)

# Ganzzahlige Division
ganzzahl_quotient = a // c
print("Ganzzahliger Quotient:", ganzzahl_quotient)

# Modulo (Rest einer Division)
rest = a % c
print("Rest:", rest)

# Potenzierung
potenz = 2 ** c
print("2 hoch 3:", potenz)

